package com.isa.morswiny.dto;

public class EventURLDto {

    private Integer eventUrlId;
    private String www;
    private String tickets;

    public Integer getEventUrlId() {
        return eventUrlId;
    }

    public void setEventUrlId(Integer eventUrlId) {
        this.eventUrlId = eventUrlId;
    }

    public String getWww() {
        return www;
    }

    public void setWww(String www) {
        this.www = www;
    }

    public String getTickets() {
        return tickets;
    }

    public void setTickets(String tickets) {
        this.tickets = tickets;
    }
}


